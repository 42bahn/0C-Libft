#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	int	i;

	i = 0;
	if (lst)
	{
		while (lst[i] && lst[i]->next)
			i++;
		lst[i]->next = new;
	}
}
